// Переменние в js
/*
    Переменные в JavaScript используются для хранения значений

    Правила именования переменных в JavaScript
    - имена переменных чувствительны к регистру (x и X - это 2 разных переменных)
    - имена преременных могут состоять из любых цифр, из любых букв латинского алфавита,
      а также символов $ и _
    - В качестве имени нельзя использовать зарезервированные и ключевые слова
            Ключевые слова в JS:
            - break
            - delete
            - function
            - return
            - typeof
            - case
            - do
            - if
            - switch
            - var
            - catch
            - else
            - in
            - this
            - void
            - continue
            - false
            - instanceof
            - throw
            - while
            - debugger
            - finally
            - new
            - true
            - with
            - default
            - for
            - null
            - try

     - Не стоит именовать переменные следующими словами:
            - implements
            - let
            - private
            - public
            - interface
            - package
            - protected
            - static
 */